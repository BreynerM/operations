def sumDosNum(n1,n2):
    return n1+n2


def restDosNum(n1,n2):
    return n1-n2


def MultiplicacionNum(n1,n2):
    return n1*n2


def DividirNum(n1,n2):
    return n1/n2


def MediaCautroNum(n1,n2,n3,n4):
    resultado = n1 + n2 + n3 + n4 / 4
    return resultado


def AreaTriangulo(b,h):
    area = b * h / 2
    return area
    

def AreaCirculo(r):
    area = PI * r**2
    return area


